/* CNT_TAG table */
CREATE TABLE CNT_TAG (
     id INT(10) NOT NULL,
     tag INT(5) NOT NULL,
    PRIMARY KEY (id)
)

 
/* CONTACT_DTLS table */
CREATE TABLE CONTACT_DTLS (
     id INT(20) NOT NULL,
     firstName VARCHAR(50) NULL DEFAULT NULL,
     middleName VARCHAR(50) NULL DEFAULT NULL,
     lastName VARCHAR(50) NULL DEFAULT NULL,
     dateOfBirth VARCHAR(50) NULL DEFAULT NULL,
	 homeAddress VARCHAR(50) NULL DEFAULT NULL,
	 workAddress VARCHAR(50) NULL DEFAULT NULL,
	 homePhone VARCHAR(50) NULL DEFAULT NULL,
	 workPhone VARCHAR(50) NULL DEFAULT NULL,
	 cellPhone VARCHAR(50) NULL DEFAULT NULL,
	 fax VARCHAR(50) NULL DEFAULT NULL,
	 emailIds VARCHAR(200) NULL DEFAULT NULL,
	 contactTag INT(20) NOT NULL,
     PRIMARY KEY (id),
     CONSTRAINT  FKTAG FOREIGN KEY (contactTag) REFERENCES CNT_TAG (id)
)
